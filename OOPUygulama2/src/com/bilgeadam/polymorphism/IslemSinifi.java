package com.bilgeadam.polymorphism;

public class IslemSinifi {

    public IslemSinifi() {
    }

    public int topla(int sayi1, int sayi2) {
        return sayi1+sayi2;
    }
    public int cikarma(int sayi1, int sayi2) {
        return sayi1-sayi2;
    }
    public float bolme(int sayi1, int sayi2) {
        return ((float) sayi1/sayi2);
    }
    public float carpma(int sayi1, int sayi2) {
        return sayi1*sayi2;
    }
}
